<p>	A mango is an edible stone fruit produced by the tropical tree Mangifera indica. 
	It is believed to have originated between northwestern Myanmar, Bangladesh, and northeastern India. 
	indica has been cultivated in South and Southeast Asia since ancient times resulting in two types of modern mango cultivars: the "Indian type" and the "Southeast Asian type". 
	Other species in the genus Mangifera also produce edible fruits that are also called "mangoes", the majority of which are found in the Malesian ecoregion.
</p>

