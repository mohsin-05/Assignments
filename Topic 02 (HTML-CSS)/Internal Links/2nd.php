<p>	A grape is a fruit, botanically a berry, of the deciduous woody vines of the flowering plant genus Vitis. Grapes are a non-climacteric type of fruit, generally occurring in clusters.
	The cultivation of grapes began perhaps 8,000 years ago, and the fruit has been used as human food over history. 
	Eaten fresh or in dried form (as raisins, currants and sultanas), grapes also hold cultural significance in many parts of the world, particularly for their role in winemaking. 
	Other grape-derived products include various types of jam, juice, vinegar and oil.

</p>

